/// <summary>
/// The 4 stages a plant progresses through during its lifecycle
/// Seed → Sprout → Sapling → Harvestable
/// </summary>
public enum GrowthStage
{
    Seed = 0,       // Initial planting stage
    Sprout = 1,     // First growth stage
    Sapling = 2,    // Second growth stage
    Harvestable = 3 // Ready to harvest
}